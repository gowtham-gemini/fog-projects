package com.assistanz.cloud.cloudstack;

/**
 *
 * @author Gowtham
 *
 */
public class NetworkInterfaceCardResponse {

    /**
     * the ID of the NetworkInterfaceCard
     */
    private String id;

    /**
     * the broadcast uri of the NetworkInterfaceCard
     */
    private String broadcastUri;

    /**
     * the gateway of the NetworkInterfaceCard
     */
    private String gateway;

    /**
     * the ip6address of the NetworkInterfaceCard
     */
    private String ip6Address;

    /**
     * the ip6cidr address of the NetworkInterfaceCard
     */
    private String ip6Cidr;

    /**
     * the ip6gateway address of the NetworkInterfaceCard
     */
    private String ip6Gateway;

    /**
     * the ip address of the NetworkInterfaceCard
     */
    private String ipAddress;

    /**
     * true if NetworkInterfaceCard is default, false otherwise
     */
    private String isDefault;

    /**
     * the isolation uri of the NetworkInterfaceCard
     */
    private String isolationUri;

    /**
     * true if NetworkInterfaceCard is default, false otherwise
     */
    private String macAddress;

    /**
     * the netmask of the NetworkInterfaceCard
     */
    private String netMask;

    /**
     * the networkid of the NetworkInterfaceCard
     */
    private String networkId;

    /**
     * the network name of the NetworkInterfaceCard
     */
    private String networkName;

    /**
     * the secondaryip address of the NetworkInterfaceCard
     */
    private String secondaryIp;

    /**
     * the traffic type of the NetworkInterfaceCard
     */
    private String trafficType;

    /**
     * the type address of the NetworkInterfaceCard
     */
    private String type;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBroadcastUri() {
        return broadcastUri;
    }

    public void setBroadcastUri(String broadcastUri) {
        this.broadcastUri = broadcastUri;
    }

    public String getGateway() {
        return gateway;
    }

    public void setGateway(String gateway) {
        this.gateway = gateway;
    }

    public String getIp6Address() {
        return ip6Address;
    }

    public void setIp6Address(String ip6Address) {
        this.ip6Address = ip6Address;
    }

    public String getIp6Cidr() {
        return ip6Cidr;
    }

    public void setIp6Cidr(String ip6Cidr) {
        this.ip6Cidr = ip6Cidr;
    }

    public String getIp6Gateway() {
        return ip6Gateway;
    }

    public void setIp6Gateway(String ip6Gateway) {
        this.ip6Gateway = ip6Gateway;
    }

    public String getIpAddress() {
        return ipAddress;
    }

    public void setIpAddress(String ipAddress) {
        this.ipAddress = ipAddress;
    }

    public String getIsDefault() {
        return isDefault;
    }

    public void setIsDefault(String isDefault) {
        this.isDefault = isDefault;
    }

    public String getIsolationUri() {
        return isolationUri;
    }

    public void setIsolationUri(String isolationUri) {
        this.isolationUri = isolationUri;
    }

    public String getMacAddress() {
        return macAddress;
    }

    public void setMacAddress(String macAddress) {
        this.macAddress = macAddress;
    }

    public String getNetMask() {
        return netMask;
    }

    public void setNetMask(String netMask) {
        this.netMask = netMask;
    }

    public String getNetworkId() {
        return networkId;
    }

    public void setNetworkId(String networkId) {
        this.networkId = networkId;
    }

    public String getNetworkName() {
        return networkName;
    }

    public void setNetworkName(String networkName) {
        this.networkName = networkName;
    }

    public String getSecondaryIp() {
        return secondaryIp;
    }

    public void setSecondaryIp(String secondaryIp) {
        this.secondaryIp = secondaryIp;
    }

    public String getTrafficType() {
        return trafficType;
    }

    public void setTrafficType(String trafficType) {
        this.trafficType = trafficType;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

 
}
