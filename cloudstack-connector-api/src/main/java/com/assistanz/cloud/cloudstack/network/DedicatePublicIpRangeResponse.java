package com.assistanz.cloud.cloudstack.network;

/**
 *
 * @author Santhosh
 */
public class DedicatePublicIpRangeResponse {

    /**
     * the ID of the VLAN IP range
     */
    private String id;

    /**
     * the account of the VLAN IP range
     */
    private String account;

    /**
     * the description of the VLAN IP range
     */
    private String description;

    /**
     * the domain name of the VLAN IP range
     */
    private String domain;

    /**
     * the domain ID of the VLAN IP range
     */
    private String domainId;

    /**
     * the end ip of the VLAN IP range
     */
    private String endIp;

    /**
     * the end ipv6 of the VLAN IP range
     */
    private String endIpv6;

    /**
     * the virtual network for the VLAN IP range
     */
    private String forVirtualNetwork;

    /**
     * the gateway of the VLAN IP range
     */
    private String gateway;

    /**
     * the cidr of IPv6 network
     */
    private String ip6Cidr;

    /**
     * the gateway of IPv6 network
     */
    private String ip6Gateway;

    /**
     * the netmask of the VLAN IP range
     */
    private String netmask;

    /**
     * the network id of vlan range
     */
    private String networkId;

    /**
     * the physical network this belongs to
     */
    private String physicalNetworkId;

    /**
     * the Pod ID for the VLAN IP range
     */
    private String podId;

    /**
     * the Pod name for the VLAN IP range
     */
    private String podName;

    /**
     * the project name of the vlan range
     */
    private String project;

    /**
     * the project id of the vlan range
     */
    private String projectId;

    /**
     * the start ip of the VLAN IP range
     */
    private String startIp;

    /**
     * the start ipv6 of the VLAN IP range
     */
    private String startIpv6;

    /**
     * the ID or VID of the VLAN
     */
    private String vlan;

    /**
     * the Zone ID of the VLAN IP range
     */
    private String zoneId;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

    public String getDomainId() {
        return domainId;
    }

    public void setDomainId(String domainId) {
        this.domainId = domainId;
    }

    public String getEndIp() {
        return endIp;
    }

    public void setEndIp(String endIp) {
        this.endIp = endIp;
    }

    public String getEndIpv6() {
        return endIpv6;
    }

    public void setEndIpv6(String endIpv6) {
        this.endIpv6 = endIpv6;
    }

    public String getForVirtualNetwork() {
        return forVirtualNetwork;
    }

    public void setForVirtualNetwork(String forVirtualNetwork) {
        this.forVirtualNetwork = forVirtualNetwork;
    }

    public String getGateway() {
        return gateway;
    }

    public void setGateway(String gateway) {
        this.gateway = gateway;
    }

    public String getIp6Cidr() {
        return ip6Cidr;
    }

    public void setIp6Cidr(String ip6Cidr) {
        this.ip6Cidr = ip6Cidr;
    }

    public String getIp6Gateway() {
        return ip6Gateway;
    }

    public void setIp6Gateway(String ip6Gateway) {
        this.ip6Gateway = ip6Gateway;
    }

    public String getNetmask() {
        return netmask;
    }

    public void setNetmask(String netmask) {
        this.netmask = netmask;
    }

    public String getNetworkId() {
        return networkId;
    }

    public void setNetworkId(String networkId) {
        this.networkId = networkId;
    }

    public String getPhysicalNetworkId() {
        return physicalNetworkId;
    }

    public void setPhysicalNetworkId(String physicalNetworkId) {
        this.physicalNetworkId = physicalNetworkId;
    }

    public String getPodId() {
        return podId;
    }

    public void setPodId(String podId) {
        this.podId = podId;
    }

    public String getPodName() {
        return podName;
    }

    public void setPodName(String podName) {
        this.podName = podName;
    }

    public String getProject() {
        return project;
    }

    public void setProject(String project) {
        this.project = project;
    }

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }

    public String getStartIp() {
        return startIp;
    }

    public void setStartIp(String startIp) {
        this.startIp = startIp;
    }

    public String getStartIpv6() {
        return startIpv6;
    }

    public void setStartIpv6(String startIpv6) {
        this.startIpv6 = startIpv6;
    }

    public String getVlan() {
        return vlan;
    }

    public void setVlan(String vlan) {
        this.vlan = vlan;
    }

    public String getZoneId() {
        return zoneId;
    }

    public void setZoneId(String zoneId) {
        this.zoneId = zoneId;
    }

}
